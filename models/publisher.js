// definindo a classe 'Publisher'
class Publisher {
  // implementando o 'construtor' da classe
  constructor (
    id,
    nome,
    pais,
    site,
    contato
  ) {
    this.id = id
    this.nome = nome,
    this.pais = pais,
    this.site = site,
    this.contato = contato
  }
}

module.exports = Publisher
// definindo a classe 'Author'
class Author {
  // implementando o 'construtor' da classe
  constructor (
    id,
	  nome,
	  genero,
	  nacionalidade,
	  nascimento
  ) {
    this.id = id
    this.nome = nome,
    this.genero = genero,
    this.nacionalidade = nacionalidade,
    this.nascimento = nascimento
  }
}

module.exports = Author